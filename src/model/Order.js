import { Schema, model } from "mongoose";

const orderSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  province: {
    type: String,
    required: true,
  },
  cart: [Object],
  total: Number,
});

const Order = model("order", orderSchema);
export default Order;
